export function MapUSA({ activeRegion = "NE", className }: MapUSAProps) {
  const prefersReducedMotion = useReducedMotion();

  const activeDot =
    DOTS.find((dot) => dot.region === activeRegion) ??
    DOTS.find((dot) => dot.region === REGION_FALLBACK)!;

  return (
    <div className={cn("relative z-0 h-full w-full", className)}>
      <svg
        aria-hidden="true"
        viewBox="0 0 960 600"
        preserveAspectRatio="xMidYMid meet"
        className="pointer-events-none block h-full w-full text-foreground/90"
        style={{ transform: "scale(1.04)" }}
      >
        <g transform="rotate(7 480 300)">
          <path
            d={US_PATH}
            fill="none"
            stroke="currentColor"
            strokeWidth={1.5}
            opacity={0.25}
          />
          {DOTS.map((dot) => {
            const isActive = dot.id === activeDot.id;

            if (prefersReducedMotion) {
              return (
                <circle
                  key={dot.id}
                  cx={dot.cx}
                  cy={dot.cy}
                  r={isActive ? 8 : 6}
                  fill={isActive ? "#e11d48" : "#fb7185"} // rose-600 vs rose-400
                  opacity={isActive ? 1 : 0.6}
                />
              );
            }

            return (
              <g key={dot.id}>
                {isActive && (
                  <motion.circle
                    cx={dot.cx}
                    cy={dot.cy}
                    r={isActive ? 8 : 6}
                    fill="#e11d48"
                    initial={{ opacity: 0.5, scale: 1 }}
                    animate={{
                      opacity: [0.5, 0, 0],
                      scale: [1, 2.5, 1],
                    }}
                    transition={{
                      duration: 2,
                      repeat: Infinity,
                      ease: "easeOut",
                    }}
                  />
                )}
                <motion.circle
                  cx={dot.cx}
                  cy={dot.cy}
                  r={isActive ? 8 : 6}
                  fill={isActive ? "#e11d48" : "#fb7185"} // rose-600 vs rose-400
                  initial={{ opacity: isActive ? 1 : 0.6, scale: 1 }}
                  animate={isActive ? {} : {
                    opacity: [0.6, 1, 0.6],
                    scale: [1, 1.2, 1],
                  }}
                  transition={{
                    duration: 2.5,
                    repeat: Infinity,
                    repeatType: "loop",
                    ease: "easeInOut",
                    delay: dot.delay,
                  }}
                />
              </g>
            );
          })}
        </g>
      </svg>
    </div>
  );
}
